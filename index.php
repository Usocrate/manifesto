				<h2>Manifeste</h2>
				<h3>Pourquoi ?</h3>
				<p>
					Le numérique est le moteur d’une révolution sociale qui va bien au-delà des problématiques technologiques.<br>
					Sa pratique transforme en profondeur les relations interpersonnelles, les rapports de production ou encore la manière dont individuellement nous nous représentons le monde.<br>
					Les anciens modes de production industriels, ne permettent plus de construire des outils numériques à la hauteur des enjeux collectifs et individuels liés à la transformation actuelle.
				</p>
				<p>
					Ce manifeste propose une méthode : replacer les usagers au centre des attentions dans le processus de conception de produit / service numérique.<br>
					Il fait de la valeur d’usage le premier critère de mesure du progrès et de l’innovation.<br>
					Il s’oppose à une vision de l’innovation faite de gadgets technologiques consommés sur un mode ludique et hautement périssables.<br>
				</p>
				<p>
					L’ambition est humaniste : celle d’améliorer la vie des gens, notamment dans leurs conditions de travail, en concevant des outils numériques adaptés, efficaces, libérant les potentiels individuels.<br>
					L’ambition est éthique : celle de mettre le numérique au service de l'intelligence humaine plutôt que d’en faire un vecteur d’aliénation et de déshumanisation.
				</p>
				<p>
					La compétitivité de notre industrie numérique est aussi au premier plan.<br>
					Être usocrate, c’est être dépité par l’absence de leaders français du numérique massivement adoptés à l'échelle mondiale ou simplement nationale; absence dûe en partie à une apathie généralisée face à la nécessité de faire de bon produits pour pouvoir être compétitifs.
				</p>
				<p>
					Donner une place centrale aux usages dans la conception de produit ou service c’est sécuriser les investissements en assurant la viabilité des services numériques développés qu’ils le soient dans un esprit de service public ou pour le secteur marchand.<br>
					Se focaliser sur les usages est le meilleur vecteur d’innovation pour les entreprises en ne les condamnant pas à copier leurs concurrents à moindre coût pour pouvoir générer du chiffre d’affaires.
				</p>
				<p>
					Pour ceux qui portent traditionnellement une démarche de conception centrée sur l’humain et les usages (ergonomes, designers), trop d’énergie est encore dépensée à convaincre les donneurs d’ordre et à obtenir les moyens de travailler.
				</p>
				<p>
					Ce manifeste veut contribuer à lever certains archaïsmes trop courants dans la conception de produits / services numériques pour libérer l’innovation et augmenter l’impact et l’utilité sociale du numérique.<br>
					Il se veut un moyen, parmi d’autres, pour les professionnels du numérique de partager cette vision.
				</p>
				<p>#design de service, #design thinking, #expérience, #usage, #empowerment</p>
				<h3>Pour qui ?</h3>
				<p>
					L’ensemble des professionnels du secteur numérique qui ont entre les mains la destinée de services numériques : chef de produit, designers, chef de projets, financeurs.
					(Prendre le créneau In-house product design là où l’Expérience utilisateur comme jadis le packaging est une affaire de gens du marketing.)
				</p>
				<p>
					Pour les signataires une adhésion consiste à :
					<ul>
						<li>Partager la vision du métier, donner du poids à un discours commun</li>
						<li>Raviver la conscience de sa responsabilité individuelle (éthique -> impact sur la vie des gens) et s’engager :<br>
						la mise en oeuvre de ce programme ne peut se faire sans transformation des organisations et des modes de production traditionnels; il s’agit de s’engager à transformer ces organisations
						mise en oeuvre dans pratiques professionnelles quotidiennes; en témoigner
						promotion de la démarche
						</li>
					</ul>
				</p>
				<h3>Principes</h3>
				<div class="row">
					<div class="col-md-4">
						<h4>Porter toute l'attention sur les usagers</h4>
						<ul>
							<li>Aller à la rencontre des usagers : pas de conception sans usagers</li>
							<li>Respecter les usagers</li>
							<li>Aimer le réel</li>
						</ul>
					</div>
					<div class="col-md-4">
						<h4>Contribuer à faire évoluer les indicateurs de réussite d’un projet</h4>
						<ul>
							<li>La valeur d’usage comme projet industriel</li>
							<li>Mesurer la réussite : appropriation, satisfaction, feedback</li>
						</ul>
					</div>
					<div class="col-md-4">
						<h4>Réaffirmer la production numérique et l’innovation comme une pratique sociale.</h4>
						<ul>
							<li>Collaborer pour l’efficacité, dépasser les conflits d’intérêts</li>
							<li>Collaborer pour la créativité</li>
							<li>Faire la promotion de la culture design</li>
							<li>Le design est un processus : pour la planification</li>
						</ul>
